import java.util.*;
class ZbirCifara {
  public static void main(String[] args) {
    Scanner input = new Scanner(System.in);
    int broj, cifra, zbir;
    System.out.print("Unesite prirodan broj: ");
    broj = input.nextInt();
    zbir = 0;
    while (broj != 0) {
      cifra = broj % 10;
      zbir = zbir + cifra;
      broj = broj / 10;
    }
    System.out.println("Zbir cifara je " + zbir);
  }
}